package model;
public class EnviaCaballC {
    
    private int aposta;
    private String user;

    public EnviaCaballC(int aposta, String user) {
        this.aposta = aposta;
        this.user = user;
    }

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public int getAposta() {
		return aposta;
	}

	public void setAposta(int aposta) {
		this.aposta = aposta;
	}

   
    
    
    
    
}
